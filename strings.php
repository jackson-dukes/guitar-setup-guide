      <h2>Changing Strings</h2>
      <img src="images/ernie-ball-strings.png">
      <p>Changing strings on an electric guitar is the process of removing old, worn out strings and replacing them with new ones. Here's how to do it:</p>

      <ol>
        <li>Gather the necessary tools: You will need a set of replacement strings, wire cutters, and a string winder (optional).</li>
        <li>Loosen the old strings: Use the tuning keys to loosen the tension on each string until it becomes slack. Then, snip off the strings as close to the tuning peg as possible, and remove them from the guitar.</li>
        <li>Clean the fretboard: With the strings removed, you can take the opportunity to clean the fretboard and guitar body. Use a soft cloth to remove any dirt, grime or dust that may have accumulated.</li>
        <li>Attach new strings: Take the first new string and thread it through the hole in the corresponding tuning peg. Pull the string through until there is a few inches of slack, then use the string winder (or your hand) to wind the string around the tuning peg. Repeat this process for all six strings, making sure to wind them in the correct direction so they don't become loose.</li>
        <li>Tune the guitar: Once all the new strings are attached, use a tuner to tune the guitar to the desired pitch. Start with the thickest string (usually the E string) and work your way down to the thinnest string (usually the high E string).</li>
        <li>Stretch and settle the strings: After tuning, stretch and settle the strings by pulling on them gently at various points along the fretboard. This will help them stay in tune better and last longer.</li>
      </ol>

      <p>With these steps, you should be able to successfully change the strings on your electric guitar. It's a good idea to change your strings regularly to keep your guitar sounding and playing its best.</p>

